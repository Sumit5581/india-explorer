export function getCityById(id) {
  return cities.find(city => city.id === id);
}

export function getPlaceById(id) {
  return places.find(place => place.id === id);
}

export function getPlacesByCity(cityId) {
  return places.filter(place => place.cityId === cityId);
}

export function getReviewsByPlace(placeId) {
  return reviews.filter(review => review.placeId === placeId);
}

export function searchCities(query) {
  const lowerQuery = query.toLowerCase();
  return cities.filter(
    city =>
      city.name.toLowerCase().includes(lowerQuery) ||
      city.state.toLowerCase().includes(lowerQuery)
  );
}

export function searchPlaces(query) {
  const lowerQuery = query.toLowerCase();
  return places.filter(
    place =>
      place.name.toLowerCase().includes(lowerQuery) ||
      place.category.toLowerCase().includes(lowerQuery)
  );
}
